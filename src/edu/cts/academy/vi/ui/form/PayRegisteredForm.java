package edu.cts.academy.vi.ui.form;

public class PayRegisteredForm {

	private String policyID;
	private String paymentID;
	private String vehicleState;
	private String policyVehicleType;
	private String policyVehicleModel;
	private String payPremium;
	private String payPremiumDate;
	private String paymentMode;
	private String payContactNo;
	
	public PayRegisteredForm(){
		
	}

	public String getPolicyID() {
		return policyID;
	}

	public void setPolicyID(String policyID) {
		this.policyID = policyID;
	}

	public String getPaymentID() {
		return paymentID;
	}

	public void setPaymentID(String paymentID) {
		this.paymentID = paymentID;
	}

	

	public String getVehicleState() {
		return vehicleState;
	}

	public void setVehicleState(String vehicleState) {
		this.vehicleState = vehicleState;
	}

	public String getPolicyVehicleType() {
		return policyVehicleType;
	}

	public void setPolicyVehicleType(String policyVehicleType) {
		this.policyVehicleType = policyVehicleType;
	}

	public String getPolicyVehicleModel() {
		return policyVehicleModel;
	}

	public void setPolicyVehicleModel(String policyVehicleModel) {
		this.policyVehicleModel = policyVehicleModel;
	}

	public String getPayPremium() {
		return payPremium;
	}

	public void setPayPremium(String payPremium) {
		this.payPremium = payPremium;
	}

	public String getPayPremiumDate() {
		return payPremiumDate;
	}

	public void setPayPremiumDate(String payPremiumDate) {
		this.payPremiumDate = payPremiumDate;
	}

	public String getPaymentMode() {
		return paymentMode;
	}

	public void setPaymentMode(String paymentMode) {
		this.paymentMode = paymentMode;
	}

	public String getPayContactNo() {
		return payContactNo;
	}

	public void setPayContactNo(String payContactNo) {
		this.payContactNo = payContactNo;
	}
}
